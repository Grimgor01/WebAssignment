			<section id="growth">
			<h2>Explain its growth or decline. How will it continue to grow?</h2>
			<p>From its inception streaming media has been progressively becoming more popular over the years. 
			With service’s catalogues featuring thousands of movies, TV shows (in the case of Netflix) and music albums (in the case of Spotify). 
			The reason for this growth is how cheap and convenient it is to watch shows or listen to music. 
			Being able to watch shows and movies whenever you want rather than having to wait for a broadcaster to play the show or song. 
			There is also the added benefit that streaming only requires access to an internet 
			connection making it possible to watch shows anywhere that there is internet. 
			It is due to these factors that streaming has grown and will continue to do so.
			</p>
			</section>
			<section id="predict">
			<h2>Predict the future of the technology</h2>
			<p>
			We will see a progression where large TV conglomerates will transition from cable networks over to streaming platforms. 
			Especially as streaming progressively becomes more popular and engaged with than cable networks. 
			We will also witness streaming to move to other mediums as well. 
			Such is the case with game streaming platforms such as GeForce Now and Stadia. 
			Where users can load up a game on one of these platform’s servers and the game is then streamed directly to the user’s device. 
			Allowing someone to play a game without needing to download the game or meet its minimum requirements. 
			This technology however suffers due to internet connections not being fast enough and the latency due 
			to the distance that the signal must travel to the user from the server. As such, as internet connections improve, 
			and this technology develops further, more people will adopt and utilise this method of streaming.
			</p>
			</section>
			<section id="compare">
			<h2>How does this compare with other technologies?</h2>
			<p>
			Similar technologies include Cable/TV, radio and video game digital distribution services.</p>
			 
        <dl>
            <dt>Cable/satellite</dt>
            <dd>As stated previously streaming platforms have been progressively becoming more popular 
        and utilised than it cable counterpart.
        This is because the programs that are shown on cable is entirely up to the broadcaster whereas with streaming platforms like 
        Netflix, users can decide what and when they wish to watch something. 
        Thus, being a more convenient and personalised experience for the user.</dd>
            <dt>Radio</dt>
            <dd>Radio’s biggest streaming competitor is Spotify. 
        Like cable, the songs and shows that are played on the radio is entirely reliant on the broadcaster. 
        The shows on radio are also, generally, not recorded. 
        Hence, it is easier for users to use a platform, like Spotify, to listen 
        to any music that they want as well as any podcasts that they wish to tune in to.</dd>
            <dt>Video game digital distribution services</dt>
            <dd>These are the platforms that streaming services like Stadia are attempting to supplement. 
        The benefit of streaming games is that you do not need to meet the minimum requirements to run or download the game. 
        The drawback is that there is delay of when the user sends an input, and the response is sent from the server. 
        You also do not own any of the games, meaning you can’t share games with your friends, 
        you are reliant on the platform for what games are on offer, you are also forced to play these games online, which is detriment if the internet is cut out.</dd>
        </dl>
